# Python Object-Oriented Programming-1

#Method: a function that is associated with a class.

class Employee:
	pass

# Instance of Employee class
emp_1 = Employee()
# Another different instance of Employee class
emp_2 = Employee()

print(emp_1)	# <__main__.Employee object at 0x011B5A10>
print(emp_2)	# <__main__.Employee object at 0x01520590>

print(emp_1 == emp_2)  # False


